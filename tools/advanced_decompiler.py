class AdvancedDecompiler:
    def __init__(self, file_path, output_dir="extracted/source_code"):
        self.file_path = file_path
        self.output_dir = output_dir
        self.file_data = None
        self.detected_language = None
        self.confidence = 0
        self.strings = []
        self.functions = []
        
