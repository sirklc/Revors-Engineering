        print("  python asm_to_source_converter.py <directory>  # T√ºm .asm dosyalarƒ±nƒ± √ßevir")
        print("\n√ñrnekler:")
        print("  python asm_to_source_converter.py program.asm")
        print("  python asm_to_source_converter.py extracted/source_code/")
        print("\n√áevrilebilir diller:")
